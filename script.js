        // Theme Management
        const ThemeManager = {
          init() {
            const savedTheme = localStorage.getItem('theme') || 'light';
            this.setTheme(savedTheme);

            const themeToggle = document.getElementById('themeToggle');
            if (themeToggle) {
              themeToggle.addEventListener('click', () => this.toggle());
            }
          },

          toggle() {
            const currentTheme = document.documentElement.getAttribute('data-theme');
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
            this.setTheme(newTheme);
          },

          setTheme(theme) {
            document.documentElement.setAttribute('data-theme', theme);
            localStorage.setItem('theme', theme);
          }
        };

        // Language Management
        const LanguageManager = {
          currentLang: 'en',

          init() {
            const savedLang = localStorage.getItem('language') || 'en';
            this.setLanguage(savedLang);

            const langToggle = document.getElementById('langToggle');
            if (langToggle) {
              langToggle.addEventListener('click', () => this.toggle());
            }
          },

          toggle() {
            const newLang = this.currentLang === 'en' ? 'zh' : 'en';
            this.setLanguage(newLang);
          },

          setLanguage(lang) {
            this.currentLang = lang;
            localStorage.setItem('language', lang);
            document.documentElement.setAttribute('lang', lang);

            document.querySelectorAll('[data-i18n]').forEach(el => {
              const key = el.getAttribute('data-i18n');
              const translation = translations[lang][key];

              if (translation) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                  el.placeholder = translation;
                } else {
                  el.textContent = translation;
                }
              }
            });

            const langToggle = document.getElementById('langToggle');
            if (langToggle) {
              const langText = langToggle.querySelector('.lang-text');
              if (langText) {
                langText.textContent = lang === 'en' ? 'EN' : 'ä¸­';
              }
            }
          },

          get(key) {
            return translations[this.currentLang][key] || key;
          }
        };

        // Number counter animation
        function animateCounter(element) {
            const target = parseInt(element.getAttribute('data-target'));
            const prefix = element.getAttribute('data-prefix') || '';
            const suffix = element.getAttribute('data-suffix') || '+';
            const duration = 2000; // 2 seconds
            const start = 0;
            const startTime = performance.now();

            function update(currentTime) {
                const elapsed = currentTime - startTime;
                const progress = Math.min(elapsed / duration, 1);

                // Easing function for smooth animation
                const easeOutQuart = 1 - Math.pow(1 - progress, 4);
                const current = Math.floor(start + (target - start) * easeOutQuart);

                element.textContent = prefix + current + suffix;

                if (progress < 1) {
                    requestAnimationFrame(update);
                } else {
                    // Add animation class when complete
                    element.classList.add('animate');
                }
            }

            requestAnimationFrame(update);
        }

        // Intersection Observer to trigger animation when stats are visible
        const observerOptions = {
            threshold: 0.5
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const statCards = entry.target.querySelectorAll('.stat-card');
                    const statNumbers = entry.target.querySelectorAll('.stat-number');

                    // Animate cards first
                    statCards.forEach((card, index) => {
                        setTimeout(() => {
                            card.classList.add('show');
                        }, index * 100);
                    });

                    // Then animate numbers
                    statNumbers.forEach((number, index) => {
                        setTimeout(() => {
                            animateCounter(number);
                        }, 400 + index * 150);
                    });

                    observer.unobserve(entry.target);
                }
            });
        }, observerOptions);

